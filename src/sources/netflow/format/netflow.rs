use super::template::{Field, Template, TemplateCache};
use super::{Context, Error, FlowSet};
use super::{apply_template, decode_data_records, decode_options_data_records};

pub fn apply_nv9_options_template(buf: &[u8], mut cx: Context) -> Result<(), Error> {
    let mut pos = 0;

    while pos + 6 < buf.len() {
        let id = (buf[pos] as u16) << 8 | (buf[pos + 1] as u16);
        let scope_count = (buf[pos + 2] as u16) << 8 | (buf[pos + 3] as u16);
        let option_count = (buf[pos + 4] as u16) << 8 | (buf[pos + 5] as u16);
        pos += 6;

        if scope_count == 0 && option_count == 0 {
            return Err(Error::NoFieldInTemplate);
        }

        let mut scopes = Vec::with_capacity(scope_count as usize / 4);
        for _ in 0..scope_count / 4 {
            let typ = (buf[pos] as u16) << 8 | (buf[pos + 1] as u16);
            let length = (buf[pos + 2] as u16) << 8 | (buf[pos + 3] as u16);
            pos += 4;

            scopes.push(Field {
                typ,
                length,
                pen: None,
            })
        }

        let mut options = Vec::with_capacity(scope_count as usize / 4);
        for _ in 0..option_count / 4 {
            let typ = (buf[pos] as u16) << 8 | (buf[pos + 1] as u16);
            let length = (buf[pos + 2] as u16) << 8 | (buf[pos + 3] as u16);
            pos += 4;

            options.push(Field {
                typ,
                length,
                pen: None,
            });
        }

        cx.add_template(id, Template::Options { scopes, options });
    }

    Ok(())
}

// https://datatracker.ietf.org/doc/html/rfc3954#section-11.1
pub struct NetflowV9Header {
    pub system_uptime: u32,
    pub unix_secs: u32,
    pub sequence_number: u32,
    pub source_id: u32,
}

pub fn parse_netflow_v9<'a>(
    buf: &'a [u8],
    templates: &mut TemplateCache,
) -> Result<(NetflowV9Header, Vec<FlowSet<'a>>), Error> {
    let version: u16 = 9;

    // check header length
    if buf.len() < 20 {
        return Err(Error::UnexpectedEof);
    }

    // https://datatracker.ietf.org/doc/html/rfc3954#section-11.1
    let count = unsafe { std::ptr::read_unaligned(buf.as_ptr().add(2) as *const u16).to_be() };
    let system_uptime =
        unsafe { std::ptr::read_unaligned(buf.as_ptr().add(4) as *const u32).to_be() };
    let unix_secs = unsafe { std::ptr::read_unaligned(buf.as_ptr().add(8) as *const u32).to_be() };
    let sequence_number =
        unsafe { std::ptr::read_unaligned(buf.as_ptr().add(12) as *const u32).to_be() };
    let source_id = unsafe { std::ptr::read_unaligned(buf.as_ptr().add(16) as *const u32).to_be() };

    let mut pos = 20;
    let mut flow_sets = Vec::with_capacity(count as usize);
    while pos + 4 < buf.len() {
        let id = (buf[pos] as u16) << 8 | (buf[pos + 1] as u16);
        let length = (buf[pos + 2] as u16) << 8 | (buf[pos + 3] as u16);

        let data = &buf[pos + 4..pos + length as usize];
        let cx = Context {
            version,
            odid: source_id,
            templates,
        };
        match id {
            0 => apply_template(data, cx)?,
            1 => apply_nv9_options_template(data, cx)?,
            id if id >= 256 => match templates.get(version, source_id, id) {
                Some(template) => {
                    let set = match template {
                        Template::Basic { fields } => FlowSet::Data {
                            template: id,
                            records: decode_data_records(data, fields)?,
                        },
                        Template::Options { scopes, options } => FlowSet::OptionsData {
                            template: id,
                            records: decode_options_data_records(data, scopes, options)?,
                        },
                    };

                    flow_sets.push(set);
                }
                None => {
                    warn!(
                        message = "unknown template",
                        template = id,
                        source_id,
                        version,
                        internal_log_rate_limit = true
                    );
                }
            },
            _ => {
                warn!(
                    message = "invalid flow set id",
                    id,
                    source_id,
                    version,
                    internal_log_rate_limit = 30,
                );
            }
        }

        pos += length as usize;
    }

    Ok((
        NetflowV9Header {
            system_uptime,
            unix_secs,
            sequence_number,
            source_id,
        },
        flow_sets,
    ))
}

